package ex_0;

public class ex_02 {
	public static void main(String[] args) {
		// 산술연산자
		
		int num1 = 7;
		// int -> 정수, num -> 변수명, = -> 대입, 7 -> 값
		int num2 = 2;
		
		System.out.println(num1 + num2);
		System.out.println(num1 - num2);
		System.out.println(num1 * num2);
		System.out.println(num1 / num2);
		System.out.println(num1 % num2);
		
		System.out.println("num1과 num2의 값은" + (num1 + num2));
	}
}
