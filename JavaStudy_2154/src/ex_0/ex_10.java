package ex_0;

public class ex_10 {

	public static void main(String[] args) {
//		삼항연산자 연습
		
		int num1 = 10;
		int num2 = 20;
		
		int result1 = num1 < num2 ? 1 : 2;	// -> 자료형 int 정수 1 : 2 가 들어감
		
		System.out.println(result1);
		
		boolean result2 = num1 > num2 ? true : false; // -> 자료형 boolean 참 거짓이 들어감.
	

	}

}
